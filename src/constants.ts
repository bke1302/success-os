// ─── Rotation helper ─────────────────────────────────────────────────────────
export function getTodayIncantation(): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86_400_000
  )
  return INCANTATIONS[dayOfYear % INCANTATIONS.length]
}
